#include <iostream>

#include "AboutDlg.h"

CAboutDlg::CAboutDlg() {}

CAboutDlg::~CAboutDlg() {}

bool CAboutDlg::Init(const Glib::RefPtr<Gtk::Builder> builder, Gtk::Window *pWin)
{
	builder->get_widget("AboutDlg", pDlg);
	if (nullptr == pDlg) {
		std::cerr << "Failed to initialize SettingsDialog!" << std::endl;
		return true;
	}
	pDlg->set_transient_for(*pWin);
	return false;
}

void CAboutDlg::Show()
{
	pDlg->run();
}
